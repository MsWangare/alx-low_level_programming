#include <stdio.h>
/** 
 * * main - main block 
 * * Description: Use `putchar` to print lowercase and then uppercase alphabet. * Return: 0
 *  */
Int main(void)
{	 
	               char c = 'a';
		                    
		                     while (c <= 'z')	
					                  {
								                             putchar(c);		
											                                c++;	
															             }

				                               putchar('\n');

							                                 return (0);
}
